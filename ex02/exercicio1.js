// Declaração de variáveis numéricas
var num_A = 10;
var num_B = 5;

// Adição
var num_C = num_A + num_B;
console.log(`Resultado da adição entre ${num_A} e ${num_B} = ${num_C}`);

// Subtração
num_C = num_A - num_B;
console.log(`Resultado da subtração entre ${num_A} e ${num_B} = ${num_C}`);

// Multiplicação
num_C = num_A * num_B;
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} = ${num_C}`);

// Divisão
num_C = num_A / num_B;
console.log(`Resultado da divisão entre ${num_A} e ${num_B} = ${num_C}`);

// Módulo
num_C = num_A % num_B;
console.log(`Resultado do módulo entre ${num_A} e ${num_B} = ${num_C}`);